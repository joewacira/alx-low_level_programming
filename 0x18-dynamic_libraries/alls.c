#include <stdio.h>
/**
 * add - adds two integers
 * @a: first num
 * @b: second num
 * Return: sumation
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first num
 * @b: second num
 * Return: subtration
 */

int sub(int a, int b)
{
        return (a - b);
}    
/**
 * div - divides two integers
 * @a: first num
 * @b: second num
 * Return: division
 */

int div(int a, int b)
{
        return (a / b);
}    
/**
 * mod - adds two integers
 * @a: first num
 * @b: second num
 * Return: remainder
 */

int mod(int a, int b)
{
        return (a % b);
}    
/*
 * mul - mutiplies two integers
 * @a: first num
 * @b: second num
 * Return: mutiple
 */

int mul(int a, int b)
{
        return (a * b);
}    
